import { EventEmitter } from 'events';

export interface LLMConfig {
  provider: 'anthropic' | 'openai' | 'local' | 'mock';
  apiKey?: string;
  baseUrl?: string;
  model: string;
  maxTokens: number;
  temperature: number;
}

export interface LLMMessage {
  role: 'system' | 'user' | 'assistant';
  content: string;
}

export interface LLMResponse {
  content: string;
  usage: {
    promptTokens: number;
    completionTokens: number;
    totalTokens: number;
  };
  model: string;
  finishReason: string;
}

export interface LLMStreamChunk {
  content: string;
  done: boolean;
}

// ==================== LLM Provider 抽象基类 ====================

export abstract class LLMProvider extends EventEmitter {
  protected config: LLMConfig;

  constructor(config: LLMConfig) {
    super();
    this.config = config;
  }

  abstract chat(messages: LLMMessage[]): Promise<LLMResponse>;
  abstract chatStream(messages: LLMMessage[]): AsyncGenerator<LLMStreamChunk>;
  abstract validate(): Promise<boolean>;
}

// ==================== Mock Provider（模拟实现） ====================

export class MockLLMProvider extends LLMProvider {
  async chat(messages: LLMMessage[]): Promise<LLMResponse> {
    const lastMessage = messages[messages.length - 1];
    const content = this.generateMockResponse(lastMessage.content);

    this.emit('response', { content, model: this.config.model });

    return {
      content,
      usage: {
        promptTokens: this.estimateTokens(messages),
        completionTokens: this.estimateTokens([{ role: 'assistant', content }]),
        totalTokens: this.estimateTokens(messages) + this.estimateTokens([{ role: 'assistant', content }]),
      },
      model: this.config.model,
      finishReason: 'stop',
    };
  }

  async *chatStream(messages: LLMMessage[]): AsyncGenerator<LLMStreamChunk> {
    const response = await this.chat(messages);
    const words = response.content.split(' ');

    for (let i = 0; i < words.length; i++) {
      yield {
        content: words[i] + ' ',
        done: i === words.length - 1,
      };
    }
  }

  async validate(): Promise<boolean> {
    return true;
  }

  private generateMockResponse(input: string): string {
    const responses: Record<string, string> = {
      'code': `// 模拟生成的代码
function generateCode() {
  console.log('Generated by AI');
  return true;
}`,
      'test': `describe('Generated Test', () => {
  it('should pass', () => {
    expect(true).toBe(true);
  });
});`,
      'architecture': `## 架构建议

基于当前需求，推荐采用分层架构：
1. 表现层：处理用户交互
2. 业务层：核心业务逻辑
3. 数据层：持久化存储

理由：关注点分离，便于测试和维护。`,
      'default': `基于您的输入，我提供以下建议：

1. 分析需求并明确目标
2. 设计合适的架构方案
3. 逐步实现并持续测试
4. 审查代码质量

请提供更多上下文以获得更精确的建议。`,
    };

    for (const [key, value] of Object.entries(responses)) {
      if (input.toLowerCase().includes(key)) {
        return value;
      }
    }

    return responses['default'];
  }

  private estimateTokens(messages: LLMMessage[]): number {
    return messages.reduce((sum, msg) => sum + Math.ceil(msg.content.length / 4), 0);
  }
}

