// ==================== LLM 工厂 ====================

export class LLMFactory {
  static createProvider(config: LLMConfig): LLMProvider {
    switch (config.provider) {
      case 'anthropic':
        return new AnthropicProvider(config);
      case 'openai':
        return new OpenAIProvider(config);
      case 'mock':
      case 'local':
      default:
        return new MockLLMProvider(config);
    }
  }
}

// ==================== Agent LLM 集成 ====================

export class AgentLLMIntegration {
  private provider: LLMProvider;

  constructor(provider: LLMProvider) {
    this.provider = provider;
  }

  /**
   * 生成代码
   */
  async generateCode(task: any, context: string): Promise<string> {
    const messages: LLMMessage[] = [
      {
        role: 'system',
        content: `你是一个专业的软件工程师。请根据任务描述生成高质量的代码。

要求：
- 遵循编码规范
- 包含适当的错误处理
- 添加必要的注释
- 生成对应的单元测试

任务信息：
${JSON.stringify(task, null, 2)}

环境上下文：
${context}`,
      },
      {
        role: 'user',
        content: `请生成任务 "${task.title}" 的代码实现。`,
      },
    ];

    const response = await this.provider.chat(messages);
    return response.content;
  }

  /**
   * 生成架构建议
   */
  async generateArchitectureSuggestion(task: any, context: string): Promise<string> {
    const messages: LLMMessage[] = [
      {
        role: 'system',
        content: `你是一个资深软件架构师。请分析需求并提供架构建议。

任务信息：
${JSON.stringify(task, null, 2)}

环境上下文：
${context}`,
      },
      {
        role: 'user',
        content: `请为 "${task.title}" 提供架构设计方案，包括技术选型、模块划分和接口设计。`,
      },
    ];

    const response = await this.provider.chat(messages);
    return response.content;
  }

  /**
   * 代码审查
   */
  async reviewCode(code: string, context: string): Promise<string> {
    const messages: LLMMessage[] = [
      {
        role: 'system',
        content: `你是一个代码审查专家。请审查以下代码并提供改进建议。

环境上下文：
${context}`,
      },
      {
        role: 'user',
        content: `请审查以下代码：\n\n\`\`\`\n${code}\n\`\`\`

关注：安全性、性能、可读性、可维护性。`,
      },
    ];

    const response = await this.provider.chat(messages);
    return response.content;
  }

  /**
   * 解释决策（可解释性）
   */
  async explainDecision(decision: any, context: string): Promise<string> {
    const messages: LLMMessage[] = [
      {
        role: 'system',
        content: `请解释以下决策的理由和依据。`,
      },
      {
        role: 'user',
        content: `决策信息：\n${JSON.stringify(decision, null, 2)}\n\n上下文：\n${context}\n\n请解释为什么做出这个决策。`,
      },
    ];

    const response = await this.provider.chat(messages);
    return response.content;
  }
}

